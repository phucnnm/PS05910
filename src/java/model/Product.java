package model;

/**
 *
 * @author Phuc
 */
public class Product {

    private String code;
    private String image;
    private String name;
    private float price;

    public Product() {
    }

    public Product(String code, String image, String name, float price) {
        this.code = code;
        this.image = image;
        this.name = name;
        this.price = price;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

}
